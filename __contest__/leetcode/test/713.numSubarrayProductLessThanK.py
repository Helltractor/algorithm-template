from typing import List


def numSubarrayProductLessThanK(nums: List[int], k: int) -> int:
    n = len(nums)
    ret = 0
    for left in range(n):
        temp = 1
        right = left
        while temp < k and right < n:
            # print(nums[left:right + 1])
            temp *= nums[right]
            # print(temp)
            if temp < k:
                right += 1
                # ret += 1
            else:
                break
        ret += right - left
    return ret


def numSubarrayProductLessThanK1(nums: List[int], k: int) -> int:
    if k <= 1: return 0
    ret, temp, left = 0, 1, 0
    for right, num in enumerate(nums):
        temp *= num
        while temp >= k:
            temp //= nums[left]
            left += 1
        ret += right - left + 1
    return ret


